function assertUnique(label, values) {
  if (values.length !== new Set(values).size) {
    throw new Error(`${label} contains duplicate entries`);
  }
}

function assertTwoWordTitles(values) {
  const invalid = values.find((title) => title.trim().split(/\s+/).length !== 2);
  if (invalid) throw new Error(`MANGA_TITLE_DATASET invalid title format: "${invalid}"`);
}

